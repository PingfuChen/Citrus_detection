class Discriminator(nn.Module):
    """
    SRGANåˆ¤åˆ«å™¨
    """

    def __init__(self, kernel_size=3, n_channels=64, n_blocks=8, fc_size=1024):
        """
        å‚æ•° kernel_size: æ‰€æœ‰å·ç§¯å±‚çš„æ ¸å¤§å°
        å‚æ•° n_channels: åˆå§‹å·ç§¯å±‚è¾“å‡ºé€šé“æ•°, åŽé¢æ¯éš”ä¸€ä¸ªå·ç§¯å±‚é€šé“æ•°ç¿»å€
        å‚æ•° n_blocks: å·ç§¯å—æ•°é‡
        å‚æ•° fc_size: å…¨è¿žæŽ¥å±‚è¿žæŽ¥æ•°
        """
        super(Discriminator, self).__init__()

        in_channels = 3

        # å·ç§¯ç³»åˆ—ï¼Œå‚ç…§è®ºæ–‡SRGANè¿›è¡Œè®¾è®¡
        conv_blocks = list()
        for i in range(n_blocks):
            out_channels = (n_channels if i is 0 else in_channels * 2) if i % 2 is 0 else in_channels
            conv_blocks.append(
                ConvolutionalBlock(in_channels=in_channels, out_channels=out_channels, kernel_size=kernel_size,
                                   stride=1 if i % 2 is 0 else 2, batch_norm=i is not 0, activation='LeakyReLu'))
            in_channels = out_channels
        self.conv_blocks = nn.Sequential(*conv_blocks)

        # å›ºå®šè¾“å‡ºå¤§å°
        self.adaptive_pool = nn.AdaptiveAvgPool2d((6, 6))

        self.fc1 = nn.Linear(out_channels * 6 * 6, fc_size)

        self.leaky_relu = nn.LeakyReLU(0.2)

        self.fc2 = nn.Linear(1024, 1)

        # æœ€åŽä¸éœ€è¦æ·»åŠ sigmoidå±‚ï¼Œå› ä¸ºPyTorchçš„nn.BCEWithLogitsLoss()å·²ç»åŒ…å«äº†è¿™ä¸ªæ­¥éª¤

    def forward(self, imgs):
        """
        å‰å‘ä¼ æ’­.

        å‚æ•° imgs: ç”¨äºŽä½œåˆ¤åˆ«çš„åŽŸå§‹é«˜æ¸…å›¾æˆ–è¶…åˆ†é‡å»ºå›¾ï¼Œå¼ é‡è¡¨ç¤ºï¼Œå¤§å°ä¸º(N, 3, w * scaling factor, h * scaling factor)
        è¿”å›ž: ä¸€ä¸ªè¯„åˆ†å€¼ï¼Œ ç”¨äºŽåˆ¤æ–­ä¸€å‰¯å›¾åƒæ˜¯å¦æ˜¯é«˜æ¸…å›¾, å¼ é‡è¡¨ç¤ºï¼Œå¤§å°ä¸º (N)
        """
        batch_size = imgs.size(0)
        output = self.conv_blocks(imgs)
        output = self.adaptive_pool(output)
        output = self.fc1(output.view(batch_size, -1))
        output = self.leaky_relu(output)
        logit = self.fc2(output)

        return logit


