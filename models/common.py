        assert maxpool_counter == i - 1 and conv_counter == j, "å½“å‰ i=%d ã€ j=%d ä¸æ»¡è¶³ VGG19 æ¨¡åž‹ç»“æž„" % (
            i, j)

        # æˆªå–ç½‘ç»œ
